    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (evt.getSource() == jButton3) 
        {
            Patient obj = new Patient();
            PatientDAO dao = new PatientDAO();
            obj.p_id = Integer.parseInt(jTextField1.getText());
            obj.name = jTextField3.getText();
            obj.cnic = jTextField5.getText();
            obj.phone_no = jTextField4.getText();
            obj.address = jTextField7.getText();
            obj.type = jTextField8.getText();
            int a = dao.Update(obj);
            if (a > 0 ) {
                JOptionPane.showMessageDialog(null,"Updated Successfully");
            }
            ResultSet rs = dao.SelectAll();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

