    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try
        {
        if (evt.getSource() == jButton5) 
        {
            Patient obj = new Patient();
            PatientDAO dao = new PatientDAO();
            obj.p_id= Integer.parseInt(jTextField1.getText());
            ResultSet rs = dao.SelectById(obj);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            int row = jTable1.getSelectedRow();
            obj.name = (String) jTable1.getValueAt(row, 1);
            obj.cnic = (String) jTable1.getValueAt(row, 2);
            obj.phone_no = (String) jTable1.getValueAt(row, 3);
            obj.address = (String) jTable1.getValueAt(row, 4);
            obj.type = (String) jTable1.getValueAt(row, 5);
        }
        }
        catch(Exception ex)
        {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

